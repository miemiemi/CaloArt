@dataclass
class Plotter:
    """An abstract class defining interface of all plotters.

    Do not use this class directly. Use ProfilePlotter or EnergyPlotter instead.

    Attributes:
        _save_dir: Directory to save the figures
        _particle_energy: Energy of the primary particle in GeV units.
        _particle_angle: Angle of the primary particle in degrees.
        _geometry: Name of the calorimeter geometry (e.g. SiW, SciPb).

    """

    _save_dir: str
    _particle_energy: int
    _particle_angle: int
    _geometry: str
    _theta: float

    def plot_and_save(self):
        pass

    def _get_formatted_conditions_str(self):
        return rf"$e^-$, {self._particle_energy} GeV, $\phi$={self._particle_angle}$^{{\circ}}$, $\theta$={self._theta}$^{{\circ}}$, {self._geometry}"

    def _save_fig(self, fig, observable_name):
        conditions_str = get_conditions_str(self._geometry, self._particle_energy, self._particle_angle, self._theta)
        plot_path = f"{self._save_dir}/{observable_name}_{conditions_str}.png"
        fig.savefig(plot_path)
        return plot_path


def _gaussian(x: np.ndarray, a: float, mu: float, sigma: float) -> np.ndarray:
    """Computes a value of a Gaussian PDF.

    Args:
        x: An argument of a function.
        a: A scaling parameter.
        mu: A mean.
        sigma: A standard deviation.

    Returns:
        A numpy array with values of a Gaussian PDF.

    """
    return a * np.exp(-((x - mu) ** 2 / (2 * sigma**2)))


def _best_fit(data: np.ndarray, bins: np.ndarray, hist: bool = False) -> Tuple[np.ndarray, np.ndarray]:
    """Finds estimated shape of a Gaussian distribution using a non-linear least squares.

    Args:
        data: A numpy array with values of observables from multiple events.
        bins: A numpy array specifying histogram bins.
        hist: If histogram is calculated. Then data is the frequencies.

    Returns:
        A tuple of two lists. Xs and Ys of predicted curve.

    """
    # Calculate histogram.
    if not hist:
        hist, _ = np.histogram(data, bins)
    else:
        hist = data

    # Choose only those bins which are nonzero. Nonzero() return a tuple of arrays. In this case it has a length = 1,
    # hence we are interested in its first element.
    indices = hist.nonzero()[0]

    # Based on previously chosen nonzero bin, calculate position of xs and ys_bar (true values) which will be used in
    # fitting procedure. Len(bins) == len(hist + 1), so we choose middles of bins as xs.
    bins_middles = (bins[:-1] + bins[1:]) / 2
    xs = bins_middles[indices]
    ys_bar = hist[indices]

    # Set initial parameters for curve fitter.
    a0 = np.max(ys_bar)
    mu0 = np.mean(xs)
    sigma0 = np.var(xs)

    # Fit a Gaussian to the prepared data.
    (a, mu, sigma), _ = curve_fit(f=_gaussian, xdata=xs, ydata=ys_bar, p0=[a0, mu0, sigma0], method="trf", maxfev=1000)

    # Calculate values of an approximation in given points and return values.
    ys = _gaussian(xs, a, mu, sigma)
    return xs, ys


@dataclass
class ProfilePlotter(Plotter):
    """Plotter responsible for preparing plots of profiles and their first and second moments.

    Attributes:
        _full_simulation: A numpy array representing a profile of data generated by Geant4.
        _ml_simulation: A numpy array representing a profile of data generated by ML model.
        _plot_gaussian: A boolean. Decides whether first and second moment should be plotted as a histogram or
            a fitted gaussian.
        _profile_type: An enum. A profile can be either azimuthal, radial or longitudinal.

    """

    _full_simulation: Profile
    _ml_simulation: Profile
    _plot_gaussian: bool = False

    def __post_init__(self):
        full_simulation_type = type(self._full_simulation)
        ml_generation_type = type(self._ml_simulation)
        assert (
            full_simulation_type == ml_generation_type
        ), "Both profiles within a ProfilePlotter must be the same type."

        # Set an attribute with profile type.
        if full_simulation_type == LongitudinalProfile:
            self._profile_type = ProfileType.LONGITUDINAL
            self._profile_type_name = "longitudinal"
        elif full_simulation_type == RadialProfile:
            self._profile_type = ProfileType.RADIAL
            self._profile_type_name = "radial"
        elif full_simulation_type == AzimuthalProfile:
            self._profile_type = ProfileType.AZIMUTHAL
            self._profile_type_name = "azimuthal"

    def _plot_and_save_customizable_histogram(
        self,
        full_simulation: np.ndarray,
        ml_simulation: np.ndarray,
        bins: np.ndarray,
        xlabel: str,
        ylabel: str,
        observable_name: str,
        plot_profile: bool = False,
        y_log_scale: bool = False,
    ) -> dict:
        """Prepares and saves a histogram for a given pair of observables.

        Args:
            full_simulation: A numpy array of observables coming from full simulation.
            ml_simulation: A numpy array of observables coming from ML simulation.
            bins: A numpy array specifying histogram bins.
            xlabel: A string. Name of x-axis on the plot.
            ylabel: A string. Name of y-axis on the plot.
            observable_name: A string. Name of plotted observable.
            plot_profile: A boolean. If set to True, full_simulation and ml_simulation are histogram weights while x is
                defined by the number of layers. This means that in order to plot histogram (and gaussian), one first
                need to create a data repeating each layer or index appropriate number of times. Should be set to True
                only while plotting profiles not first or second moments.
            y_log_scale: A boolean. Used log scale on y-axis is set to True.

        Returns:
            Dictionary with path to the saved plot and respective EMD between data generated by Geant4 and ML model.

        """
        fig, (main_ax, ratio_ax) = plt.subplots(
            2, 1, figsize=(15, 10), clear=True, sharex="all", gridspec_kw={"height_ratios": [3, 1]}
        )
        fig.subplots_adjust(hspace=0.1, wspace=0.1)

        # Plot histograms.
        if plot_profile:
            emd = wasserstein_distance(
                np.arange(len(full_simulation)), np.arange(len(ml_simulation)), full_simulation, ml_simulation
            )
            hep.histplot(
                full_simulation,
                bins=bins,
                ax=main_ax,
                label=FULL_SIM_LABEL,
                histtype="fill" if FULL_SIM_HISTTYPE == "stepfilled" else FULL_SIM_HISTTYPE,
                color=FULL_SIM_HISTOGRAM_COLOR,
                binticks=True,
                edgecolor=FULL_SIM_HISTOGRAM_EDGE_COLOR,
                linewidth=1,
            )
            hep.histplot(
                ml_simulation,
                bins=bins,
                ax=main_ax,
                label=f"{ML_SIM_LABEL}, EMD: {emd:.3f}",
                histtype=ML_SIM_HISTTYPE,
                color=ML_SIM_HISTOGRAM_COLOR,
                binticks=True,
            )
            main_ax.tick_params(axis="x", which="both", bottom=True, top=True, labelbottom=False)

            # For using it later for the ratios.
            energy_full_sim, energy_ml_sim = full_simulation, ml_simulation
        else:
            energy_full_sim, _ = np.histogram(full_simulation, bins=bins)
            energy_ml_sim, _ = np.histogram(ml_simulation, bins=bins)
            emd = wasserstein_distance(full_simulation, ml_simulation)
            hep.histplot(
                energy_full_sim,
                bins=bins,
                ax=main_ax,
                label=FULL_SIM_LABEL,
                histtype="fill" if FULL_SIM_HISTTYPE == "stepfilled" else FULL_SIM_HISTTYPE,
                color=FULL_SIM_HISTOGRAM_COLOR,
                edgecolor=FULL_SIM_HISTOGRAM_EDGE_COLOR,
                linewidth=1,
            )
            hep.histplot(
                energy_ml_sim,
                bins=bins,
                ax=main_ax,
                label=f"{ML_SIM_LABEL}, EMD: {emd:.3f}",
                histtype=ML_SIM_HISTTYPE,
                color=ML_SIM_HISTOGRAM_COLOR,
            )

        # Plot Gaussians if needed.
        if self._plot_gaussian:
            if plot_profile:
                (xs_full_sim, ys_full_sim) = _best_fit(full_simulation, bins, hist=True)
                (xs_ml_sim, ys_ml_sim) = _best_fit(ml_simulation, bins, hist=True)
            else:
                (xs_full_sim, ys_full_sim) = _best_fit(full_simulation, bins)
                (xs_ml_sim, ys_ml_sim) = _best_fit(ml_simulation, bins)
            main_ax.plot(xs_full_sim, ys_full_sim, color=FULL_SIM_GAUSSIAN_COLOR, label=FULL_SIM_LABEL)
            main_ax.plot(xs_ml_sim, ys_ml_sim, color=ML_SIM_GAUSSIAN_COLOR, label=ML_SIM_LABEL)

        if y_log_scale:
            main_ax.set_yscale("log")

        main_ax.legend(loc="best", bbox_to_anchor=(0.6, 0.0, 0.4, 1.0))
        main_ax.set_ylabel(ylabel)
        main_ax.set_title(self._get_formatted_conditions_str(), loc="right")

        # Calculate ratios.
        energy_ml_sim = energy_ml_sim.astype(np.float32)
        energy_full_sim = energy_full_sim.astype(np.float32)
        ratio = np.divide(
            energy_ml_sim, energy_full_sim, out=np.ones_like(energy_ml_sim), where=(energy_full_sim != 0)
        )
        # Since len(bins) == 1 + data, we calculate middles of bins as xs.
        bins_middles = (bins[:-1] + bins[1:]) / 2
        ratio_ax.plot(bins_middles, ratio, "-", color=ML_SIM_HISTOGRAM_COLOR)
        ratio_ax.set_xlabel(xlabel)
        ratio_ax.set_ylabel("Ratio")
        ratio_ax.axhline(y=1.0, color="black", linestyle="--", linewidth=0.5)

        plot_path = self._save_fig(fig, observable_name)
        plt.close(fig)

        return {"plot_path": plot_path, "emd": emd}

    def _plot_total_energy_per_layer(self) -> dict:
        """Plots profile of an observable.

        Returns:
            Dictionary with path to the saved plot and respective EMD between data generated by Geant4 and ML model.
        """
        full_simulation_energy = self._full_simulation.calc_total_energy_per_layer()
        ml_simulation_energy = self._ml_simulation.calc_total_energy_per_layer()

        if self._profile_type == ProfileType.LONGITUDINAL:
            observable_name = "LongTotalEnergy"
            xlabel = r"$z$ index"
            # matplotlib will include the right-limit for the last bar, hence extending by 1.
            bins = np.arange(GEOMETRY.N_CELLS_Z + 1)
        elif self._profile_type == ProfileType.RADIAL:
            observable_name = "RadTotalEnergy"
            xlabel = r"$r$ index"
            bins = np.arange(GEOMETRY.N_CELLS_R + 1)
        elif self._profile_type == ProfileType.AZIMUTHAL:
            observable_name = "AzimTotalEnergy"
            xlabel = r"$\varphi$ index"
            bins = np.arange(GEOMETRY.N_CELLS_PHI + 1)

        ylabel = "Deposited energy [MeV]"

        plot_and_emd = self._plot_and_save_customizable_histogram(
            full_simulation_energy, ml_simulation_energy, bins, xlabel, ylabel, observable_name, plot_profile=True
        )
        return {observable_name: plot_and_emd}

